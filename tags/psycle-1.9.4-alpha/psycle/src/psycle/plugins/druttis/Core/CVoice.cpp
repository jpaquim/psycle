#include "CVoice.h"
//============================================================================
//	Constructor
//============================================================================
CVoice::CVoice()
{
	active = false;
}
//============================================================================
//	Destructor
//============================================================================
CVoice::~CVoice()
{
}
//============================================================================
//	Stop
//============================================================================
void CVoice::Stop()
{
	active = false;
}
//============================================================================
//	NoteOff
//============================================================================
void CVoice::NoteOff()
{
	active = false;
}
//============================================================================
//	NoteOn
//============================================================================
void CVoice::NoteOn(int note, int volume)
{
	phase = 0.0f;
	speed = CDsp::GetFreq((float) note, 1, globals->samplingrate);
	active = true;
}
